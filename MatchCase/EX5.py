#Tradutor de cores
cor = input("Digite o a cor que deseja saber seu nome em ingles: ")

match cor:
    case "vermelho":
        print(f"A cor {cor} em inglês é RED")

    case "azul":
        print(f"A cor {cor} em inglês é BLUE")

    case "verde":
        print(f"A cor {cor} em inglês é GREEN")

    case "amarelo":
        print(f"A cor {cor} em inglês é YELLOW")

